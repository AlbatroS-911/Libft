int	ft_atoi(const char *nptr)
{
	int	i;
	int nbr;
	int sign;

	i = 0;
	sign = 1;
	nbr = 0;

	while ((nptr[i] == 32) || (nptr[i] >= 7 && nptr[i] <= 13))
		i++;
	while (nptr[i] == '+' || nptr[i] == '-')
	{
		if(nptr[i] == '-')
			sign = sign * -1;
		i++;
	}
	while (nptr[i] >= '0' && nptr[i] <= '9')
	{
		nbr = nbr * 10 + (nptr[i] - '0');
		i++;
	}
	return (nbr * sign);
}
